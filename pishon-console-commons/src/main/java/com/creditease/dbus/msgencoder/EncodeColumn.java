package com.creditease.dbus.msgencoder;

import java.util.Date;

/**
 * Created by zhangyf on 16/11/9.
 */
public class EncodeColumn {
    private long id;
    private long tableId;
    private String fieldName;
    private int length;
    private String encodeType;
    private Long pluginId;
    private String encodeParam;
    private boolean truncate;
    private String desc;
    private Date updateTime;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public long getTableId() {
        return tableId;
    }

    public void setTableId(long tableId) {
        this.tableId = tableId;
    }

    public String getFieldName() {
        return fieldName;
    }

    public void setFieldName(String fieldName) {
        this.fieldName = fieldName;
    }
    public String getEncodeType() {
        return encodeType;
    }

    public void setEncodeType(String encodeType) {
        this.encodeType = encodeType;
    }

    public String getEncodeParam() {
        return encodeParam;
    }

    public boolean isTruncate() {
        return truncate;
    }

    public void setTruncate(boolean truncate) {
        this.truncate = truncate;
    }

    public void setEncodeParam(String encodeParam) {
        this.encodeParam = encodeParam;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public Date getUpdateTime() {
        return updateTime;
    }

    public void setUpdateTime(Date updateTime) {
        this.updateTime = updateTime;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

    public Long getPluginId() {
        return pluginId;
    }

    public void setPluginId(Long pluginId) {
        this.pluginId = pluginId;
    }

    @Override
    public String toString() {
        return "EncodeColumn{" +
                "fieldName='" + fieldName + '\'' +
                ", encodeType='" + encodeType + '\'' +
                ", pluginId=" + pluginId +
                ", encodeParam='" + encodeParam + '\'' +
                ", truncate=" + truncate +
                '}';
    }
}
